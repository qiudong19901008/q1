    <p>足少阴经发生疟疾，会使人呕吐厉害，发寒发热，热多寒少，总想关闭门窗呆在屋里，此病很难治愈。</p>
    <p>足厥阴经发生疟疾，会令人腰痛，少腹部位胀满，小便不通畅。症状虽类似癃病但却不是癃病，小便虽然不畅但次数很多，容易恐惧，气也不足。此病可取足厥阴经的太冲穴针刺治疗。</p>
